#!/bin/bash

# –ü–æ–ª—É—á–∞–µ–º –ø—É—Ç—å –∫ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏ —Å–∫—Ä–∏–ø—Ç–∞
SCRIPT_DIR=$(dirname "$(realpath "$0")")

# –û–ø—Ä–µ–¥–µ–ª—è–µ–º –ø—É—Ç–∏ –¥–ª—è –ø–∞–ø–æ–∫ –ø—Ä–æ–µ–∫—Ç–∞ –∏ –ª–æ–≥–æ–≤
PROJECT_DIR="$SCRIPT_DIR/pyChainLite"
LOG_DIR="$PROJECT_DIR/logs"
LOG_FILE="$LOG_DIR/install-update.log"
BLOCKCHAIN_DIR="$PROJECT_DIR/blockchains"
PARENT_DIR=$(dirname "$SCRIPT_DIR")

# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –∑–∞–ø–∏—Å–∏ –≤ –ª–æ–≥ —Å –¥–∞—Ç–æ–π –∏ –≤—Ä–µ–º–µ–Ω–µ–º
log() {
    echo "[$(date '+%Y-%m-%d %H:%M:%S')] $1" | tee -a "$LOG_FILE"
}

# –°–æ–∑–¥–∞–µ–º –ø–∞–ø–∫—É –¥–ª—è –ª–æ–≥–æ–≤, –µ—Å–ª–∏ –æ–Ω–∞ –Ω–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç
if [ ! -d "$LOG_DIR" ]; then
    log "–°–æ–∑–¥–∞–Ω–∏–µ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏ –¥–ª—è –ª–æ–≥–æ–≤..."
    mkdir -p "$LOG_DIR" || { log "–û—à–∏–±–∫–∞ —Å–æ–∑–¥–∞–Ω–∏—è –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏ –¥–ª—è –ª–æ–≥–æ–≤."; exit 1; }
fi

# –°–æ–∑–¥–∞–µ–º —Ñ–∞–π–ª –ª–æ–≥–∞, –µ—Å–ª–∏ –æ–Ω –Ω–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç
if [ ! -f "$LOG_FILE" ];then
    log "–°–æ–∑–¥–∞–Ω–∏–µ —Ñ–∞–π–ª–∞ –ª–æ–≥–∞..."
    touch "$LOG_FILE" || { log "–û—à–∏–±–∫–∞ —Å–æ–∑–¥–∞–Ω–∏—è —Ñ–∞–π–ª–∞ –ª–æ–≥–∞."; exit 1; }
fi

log "–ó–∞–ø—É—Å–∫ —É—Å—Ç–∞–Ω–æ–≤–∫–∏/–æ–±–Ω–æ–≤–ª–µ–Ω–∏—è"

# –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞–ª–∏—á–∏—è Python
if ! command -v python3 &> /dev/null; then
    log "Python3 –Ω–µ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω. –£—Å—Ç–∞–Ω–æ–≤–∏—Ç–µ Python –≤–µ—Ä—Å–∏–∏ 3.12.x."
    exit 1
fi

log "Python —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω: $(python3 --version)"

# –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞–ª–∏—á–∏—è Git
if ! command -v git &> /dev/null; then
    log "Git –Ω–µ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω. –£—Å—Ç–∞–Ω–æ–≤–∏—Ç–µ Git –¥–ª—è –ø—Ä–æ–¥–æ–ª–∂–µ–Ω–∏—è."
    exit 1
fi

log "Git —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω: $(git --version)"

# –ü–µ—Ä–µ–º–µ—â–µ–Ω–∏–µ –ø–∞–ø–∫–∏ —Å –±–ª–æ–∫—á–µ–π–Ω–∞–º–∏ –≤ —Ä–æ–¥–∏—Ç–µ–ª—å—Å–∫—É—é –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—é –ø–µ—Ä–µ–¥ –æ–±–Ω–æ–≤–ª–µ–Ω–∏–µ–º
if [ -d "$BLOCKCHAIN_DIR" ]; then
    log "–ü–µ—Ä–µ–º–µ—â–∞—é –ø–∞–ø–∫—É —Å –±–ª–æ–∫—á–µ–π–Ω–∞–º–∏ –≤ —Ä–æ–¥–∏—Ç–µ–ª—å—Å–∫—É—é –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—é..."
    mv "$BLOCKCHAIN_DIR" "$PARENT_DIR" || { log "–û—à–∏–±–∫–∞ –ø–µ—Ä–µ–º–µ—â–µ–Ω–∏—è –±–ª–æ–∫—á–µ–π–Ω–æ–≤."; exit 1; }
    log "–ü–∞–ø–∫–∞ —Å –±–ª–æ–∫—á–µ–π–Ω–∞–º–∏ –ø–µ—Ä–µ–º–µ—â–µ–Ω–∞ –≤ $PARENT_DIR."
else
    log "–ü–∞–ø–∫–∞ —Å –±–ª–æ–∫—á–µ–π–Ω–∞–º–∏ –Ω–µ –Ω–∞–π–¥–µ–Ω–∞."
fi

# –ï—Å–ª–∏ –ø—Ä–æ–µ–∫—Ç–∞ –Ω–µ—Ç –∏–ª–∏ —á–∞—Å—Ç—å —Ñ–∞–π–ª–æ–≤ –æ—Ç—Å—É—Ç—Å—Ç–≤—É–µ—Ç, –∫–ª–æ–Ω–∏—Ä—É–µ–º —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏–π –∑–∞–Ω–æ–≤–æ
if [ ! -d "$PROJECT_DIR/.git" ] || [ ! -f "$PROJECT_DIR/create_dirs.sh" ]; then
    log "–ß–∞—Å—Ç—å —Ñ–∞–π–ª–æ–≤ –ø—Ä–æ–µ–∫—Ç–∞ –æ—Ç—Å—É—Ç—Å—Ç–≤—É–µ—Ç –∏–ª–∏ –ø—Ä–æ–µ–∫—Ç –Ω–µ —è–≤–ª—è–µ—Ç—Å—è git-—Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏–µ–º. –ö–ª–æ–Ω–∏—Ä—É—é –∑–∞–Ω–æ–≤–æ..."
    
    # –£–¥–∞–ª—è–µ–º –ø—Ä–æ–µ–∫—Ç, –µ—Å–ª–∏ –æ–Ω –Ω–µ–ø–æ–ª–Ω—ã–π, –∏ –∫–ª–æ–Ω–∏—Ä—É–µ–º –∑–∞–Ω–æ–≤–æ
    rm -rf "$PROJECT_DIR"
    git clone https://github.com/giteed/pyChainLite.git "$PROJECT_DIR" || { log "–û—à–∏–±–∫–∞ –∫–ª–æ–Ω–∏—Ä–æ–≤–∞–Ω–∏—è —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏—è."; exit 1; }
    
    log "–ö–ª–æ–Ω–∏—Ä–æ–≤–∞–Ω–∏–µ –∑–∞–≤–µ—Ä—à–µ–Ω–æ —É—Å–ø–µ—à–Ω–æ."
fi

# –ü–µ—Ä–µ–º–µ—â–∞–µ–º—Å—è –≤ –ø–∞–ø–∫—É –ø—Ä–æ–µ–∫—Ç–∞
cd "$PROJECT_DIR" || { log "–û—à–∏–±–∫–∞: –Ω–µ —É–¥–∞–µ—Ç—Å—è –∑–∞–π—Ç–∏ –≤ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—é –ø—Ä–æ–µ–∫—Ç–∞."; exit 1; }

# –ó–∞–ø—É—Å–∫ —Å–∫—Ä–∏–ø—Ç–∞ –¥–ª—è —Å–æ–∑–¥–∞–Ω–∏—è –ø–∞–ø–æ–∫ src –∏ tests
if [ ! -f "$PROJECT_DIR/create_dirs.sh" ]; then
    log "–û—à–∏–±–∫–∞: —Å–∫—Ä–∏–ø—Ç create_dirs.sh –Ω–µ –Ω–∞–π–¥–µ–Ω –¥–∞–∂–µ –ø–æ—Å–ª–µ –∫–ª–æ–Ω–∏—Ä–æ–≤–∞–Ω–∏—è."
    exit 1
fi

log "–ü—Ä–æ–≤–µ—Ä–∫–∞ –∏ —Å–æ–∑–¥–∞–Ω–∏–µ –Ω–µ–æ–±—Ö–æ–¥–∏–º—ã—Ö –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–π..."
chmod +x "$PROJECT_DIR/create_dirs.sh"
chmod +x "$PROJECT_DIR/update-and-start.sh"
"$PROJECT_DIR/create_dirs.sh"

# –ï—Å–ª–∏ –ø—Ä–æ–µ–∫—Ç —É–∂–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç, –≤—ã–ø–æ–ª–Ω—è–µ–º –æ–±–Ω–æ–≤–ª–µ–Ω–∏–µ
if [ -d "$PROJECT_DIR/.git" ]; then
    log "–ü—Ä–æ–µ–∫—Ç —É–∂–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç, –≤—ã–ø–æ–ª–Ω—è–µ—Ç—Å—è –æ–±–Ω–æ–≤–ª–µ–Ω–∏–µ..."
    cd "$PROJECT_DIR" || { log "–û—à–∏–±–∫–∞: –Ω–µ —É–¥–∞–µ—Ç—Å—è –∑–∞–π—Ç–∏ –≤ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—é –ø—Ä–æ–µ–∫—Ç–∞."; exit 1; }

    # –ü—Ä–∏–Ω—É–¥–∏—Ç–µ–ª—å–Ω–æ —Å–±—Ä–∞—Å—ã–≤–∞–µ–º –∏–∑–º–µ–Ω–µ–Ω–∏—è –∏ –æ–±–Ω–æ–≤–ª—è–µ–º –ø—Ä–æ–µ–∫—Ç
    log "–°–±—Ä–æ—Å –ª–æ–∫–∞–ª—å–Ω—ã—Ö –∏–∑–º–µ–Ω–µ–Ω–∏–π..."
    git reset --hard HEAD || { log "–û—à–∏–±–∫–∞ –ø—Ä–∏ —Å–±—Ä–æ—Å–µ –∏–∑–º–µ–Ω–µ–Ω–∏–π."; exit 1; }
    
    log "–û–±–Ω–æ–≤–ª–µ–Ω–∏–µ —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏—è..."
    git pull origin main || { log "–û—à–∏–±–∫–∞ –ø—Ä–∏ –æ–±–Ω–æ–≤–ª–µ–Ω–∏–∏ —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏—è."; exit 1; }

    log "–£—Å—Ç–∞–Ω–æ–≤–∫–∞ –ø—Ä–∞–≤ –Ω–∞ –≤—ã–ø–æ–ª–Ω–µ–Ω–∏–µ –¥–ª—è start.sh –∏ update-and-start.sh..."
    chmod +x "$PROJECT_DIR/start.sh" || { log "–û—à–∏–±–∫–∞ –ø—Ä–∏ —É—Å—Ç–∞–Ω–æ–≤–∫–µ –ø—Ä–∞–≤ –Ω–∞ –≤—ã–ø–æ–ª–Ω–µ–Ω–∏–µ –¥–ª—è start.sh."; exit 1; }
    chmod +x "$PROJECT_DIR/update-and-start.sh" || { log "–û—à–∏–±–∫–∞ –ø—Ä–∏ —É—Å—Ç–∞–Ω–æ–≤–∫–µ –ø—Ä–∞–≤ –Ω–∞ –≤—ã–ø–æ–ª–Ω–µ–Ω–∏–µ –¥–ª—è update-and-start.sh."; exit 1; }
fi

# –£—Å—Ç–∞–Ω–æ–≤–∫–∞ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–µ–π
if [ -f "$PROJECT_DIR/requirements.txt" ]; then
    log "–£—Å—Ç–∞–Ω–æ–≤–∫–∞ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–µ–π –∏–∑ requirements.txt..."
    pip install --upgrade pip || { log "–û—à–∏–±–∫–∞ –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è pip."; exit 1; }
    pip install -r requirements.txt || { log "–û—à–∏–±–∫–∞ —É—Å—Ç–∞–Ω–æ–≤–∫–∏ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–µ–π."; exit 1; }
else
    log "–§–∞–π–ª requirements.txt –Ω–µ –Ω–∞–π–¥–µ–Ω. –£—Å—Ç–∞–Ω–æ–≤–∫–∞ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–µ–π –Ω–µ–≤–æ–∑–º–æ–∂–Ω–∞."
    exit 1
fi

log "–£—Å—Ç–∞–Ω–æ–≤–∫–∞ –∏–ª–∏ –æ–±–Ω–æ–≤–ª–µ–Ω–∏–µ –∑–∞–≤–µ—Ä—à–µ–Ω—ã —É—Å–ø–µ—à–Ω–æ."

# –ü—Ä–µ–¥–ª–∞–≥–∞–µ–º –ø–µ—Ä–µ–º–µ—Å—Ç–∏—Ç—å –ø–∞–ø–∫—É —Å –±–ª–æ–∫—á–µ–π–Ω–∞–º–∏ –æ–±—Ä–∞—Ç–Ω–æ
if [ -d "$PARENT_DIR/blockchains" ]; then
    echo "–ñ–µ–ª–∞–µ—Ç–µ –ø–µ—Ä–µ–º–µ—Å—Ç–∏—Ç—å –ø–∞–ø–∫—É —Å –±–ª–æ–∫—á–µ–π–Ω–∞–º–∏ –æ–±—Ä–∞—Ç–Ω–æ –≤ –ø—Ä–æ–µ–∫—Ç? (Y/n)"
    read -r move_back_choice
    if [ "$move_back_choice" != "n" ]; then
        log "–ü–µ—Ä–µ–º–µ—â–∞—é –ø–∞–ø–∫—É —Å –±–ª–æ–∫—á–µ–π–Ω–∞–º–∏ –æ–±—Ä–∞—Ç–Ω–æ –≤ –ø—Ä–æ–µ–∫—Ç..."
        mv "$PARENT_DIR/blockchains" "$PROJECT_DIR" || { log "–û—à–∏–±–∫–∞ –ø–µ—Ä–µ–º–µ—â–µ–Ω–∏—è –±–ª–æ–∫—á–µ–π–Ω–æ–≤ –æ–±—Ä–∞—Ç–Ω–æ."; exit 1; }
        log "–ü–∞–ø–∫–∞ —Å –±–ª–æ–∫—á–µ–π–Ω–∞–º–∏ —É—Å–ø–µ—à–Ω–æ –ø–µ—Ä–µ–º–µ—â–µ–Ω–∞ –æ–±—Ä–∞—Ç–Ω–æ –≤ –ø—Ä–æ–µ–∫—Ç."
    else
        log "–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –≤—ã–±—Ä–∞–ª –Ω–µ –ø–µ—Ä–µ–º–µ—â–∞—Ç—å –±–ª–æ–∫—á–µ–π–Ω—ã –æ–±—Ä–∞—Ç–Ω–æ."
        echo "üü¢ [green]–ü–∞–ø–∫–∞ —Å –±–ª–æ–∫—á–µ–π–Ω–∞–º–∏ –æ—Å—Ç–∞–ª–∞—Å—å –≤ $PARENT_DIR. –ü–µ—Ä–µ–º–µ—Å—Ç–∏—Ç–µ –µ—ë –≤—Ä—É—á–Ω—É—é –ø—Ä–∏ –Ω–µ–æ–±—Ö–æ–¥–∏–º–æ—Å—Ç–∏.[/green]"
    fi
fi

# –ó–∞–≤–µ—Ä—à–µ–Ω–∏–µ —Ä–∞–±–æ—Ç—ã –≤–∏—Ä—Ç—É–∞–ª—å–Ω–æ–≥–æ –æ–∫—Ä—É–∂–µ–Ω–∏—è, –µ—Å–ª–∏ –æ–Ω–æ –±—ã–ª–æ –∞–∫—Ç–∏–≤–∏—Ä–æ–≤–∞–Ω–æ
if [[ "$VIRTUAL_ENV" != "" ]]; then
    deactivate
else
    log "–í–∏—Ä—Ç—É–∞–ª—å–Ω–æ–µ –æ–∫—Ä—É–∂–µ–Ω–∏–µ –Ω–µ –±—ã–ª–æ –∞–∫—Ç–∏–≤–∏—Ä–æ–≤–∞–Ω–æ, –∫–æ–º–∞–Ω–¥–∞ deactivate –ø—Ä–æ–ø—É—â–µ–Ω–∞."
fi

cd "$SCRIPT_DIR"

log "–°–∫—Ä–∏–ø—Ç –∑–∞–≤–µ—Ä—à–∏–ª —Ä–∞–±–æ—Ç—É. –õ–æ–≥ –∑–∞–ø–∏—Å–∞–Ω –≤ $LOG_FILE."

